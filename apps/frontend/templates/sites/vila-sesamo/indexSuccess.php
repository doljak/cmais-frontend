<script>
  $(document).ready(function() {
    //carrossel personagens
    $('#carrossel-p').responsiveCarousel({
      target:'#carrossel-p .slider-target',
      unitElement:'#carrossel-p .slider-target > li',
      mask:'#carrossel-p .slider-mask',
      easing:'linear',
      dragEvents:true,
      speed:200,
      slideSpeed:1000,
      responsiveUnitSize : function() {
        return 2;
      },
      step : -1,
      onShift : function(i) {
        var $current = $('#selector li a[rel=frame_' + i + ']');
        $('#selector li a').removeClass('current');
        $current.addClass('current');
      }
    });
    
    if(screen.width > 980){
      $('#carrossel-p').mouseenter(function(){
        $('.arrow').fadeIn('fast');
      });
      $('#carrossel-p').mouseleave(function(){
         $('.arrow').fadeOut('fast');
      });
    };
    if(navigator.appName!='Microsoft Internet Explorer')
    {
      //carrossel personagens redraw pra tablet e celular
      window.addEventListener('load', function() {
        $('.carrossel-p').responsiveCarousel('redraw');
      });
      window.addEventListener("orientationchange", function() {
        $('.carrossel-p').responsiveCarousel('redraw');
      }, false);
      window.addEventListener("resize", function() {
        $('.carrossel-p').responsiveCarousel('redraw');
      }, false);
      //carrossel personagens redraw pra tablet e celular
    }
    //carrossel personagens
    
    //menu principal
    var w_default = 62;
    $('.header-bar ul li').mouseenter(function() {
      var el = $("." + $(this).attr("class") + " .fundo");
      var elBorda = $("." + $(this).attr("class") + " .borda");
      var w_time = $(this).attr("data-time");
      var w_button = $(this).attr("data-width");
      
      el.stop();
      $(this).stop();
      
      el.show();
      elBorda.show();
      $(this).animate({
        width: w_button
      }, w_time);
      el.animate({
        width:  w_button
      }, w_time);
    });
    
    $('.header-bar ul li').mouseleave(function() {
      var el = $("." + $(this).attr("class") + " .fundo");
      var elBorda = $("." + $(this).attr("class") + " .borda");
      var w_back = $(this).attr("data-back")
      
      $(this).stop();
      el.stop();
      
      $(this).animate({
        width: w_default
      }, w_back);
      el.animate({
        width: w_default
      }, w_back, function(){
        if(el.width() == w_default){
          el.hide();
          elBorda.hide();
        }
      });
      //menu principal
    });

  });
</script>