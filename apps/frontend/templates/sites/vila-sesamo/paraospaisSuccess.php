      dragEvents:         true,
      step:-1,
      onShift:function (i) {
          var $current = $('#selector-interna-personagem li a[rel=frame_' + i + ']');
          $('#selector-interna-personagem li a').removeClass('current');
          $current.addClass('current');
      },
      slideSpeed: 8000
  });
  
