<script>

  $(document).ready(function() {
    //carrossel personagens
    $('#carrossel-p').responsiveCarousel({
      target:'#carrossel-p .slider-target',
      unitElement:'#carrossel-p .slider-target > li',
      mask:'#carrossel-p .slider-mask',
      easing:'linear',
      dragEvents:true,
      speed:200,
      slideSpeed:1000,
      responsiveUnitSize : function() {
        return 2;
      },
      step : -1,
      onShift : function(i) {
        var $current = $('#selector li a[rel=frame_' + i + ']');
        $('#selector li a').removeClass('current');
        $current.addClass('current');
      }
    });
    
    if(screen.width > 980){
      $('#carrossel-p').mouseenter(function(){
        $('.arrow').fadeIn('fast');
      });
      $('#carrossel-p').mouseleave(function(){
         $('.arrow').fadeOut('fast');
      });
    };
    //carrossel personagens
    
    //menu principal
    var w_default = 62;
    $('.header-bar ul li').mouseenter(function() {
      var el = $("." + $(this).attr("class") + " .fundo");
      var elBorda = $("." + $(this).attr("class") + " .borda");
      var w_button = $(this).attr("data-width");
      
      el.stop();
      $(this).stop();
      
      el.show();
      elBorda.show();
      $(this).animate({
        width: w_button
      }, 800);
      el.animate({
        width:  w_button
      }, 800);
    });
    
    $('.header-bar ul li').mouseleave(function() {
      var el = $("." + $(this).attr("class") + " .fundo");
      var elBorda = $("." + $(this).attr("class") + " .borda");
      
      $(this).stop();
      el.stop();
      
      $(this).animate({
        width: w_default
      }, 500);
      el.animate({
        width: w_default
      }, 500, function(){
        if(el.width() == w_default){
          el.hide();
          elBorda.hide();
        }
      });
      //menu principal
    });

  });
  
  //carrossel personagens
  window.addEventListener('load', function() {
    $('.carrossel-p').responsiveCarousel('redraw');
  });
  window.addEventListener("orientationchange", function() {
    $('.carrossel-p').responsiveCarousel('redraw');
  }, false);
  window.addEventListener("resize", function() {
    $('.carrossel-p').responsiveCarousel('redraw');
  }, false);
  //carrossel personagens
</script>