        <textarea readonly name="regulamento" id="regulamento" style="width: 100%; height: 200px; font-size: 14px; padding:15px; display: none;" />REGULAMENTO PARA PARTICIPAÇÃO NO PROGRAMA “QUEM SABE, SABE”

A Fundação Padre Anchieta – Centro Paulista de Rádio e TV Educativas, através de sua emissora de televisão “TV Cultura – Canal 2” (doravante simplesmente “Tv Cultura”), está produzindo o Programa de Televisão denominado “Quem Sabe, Sabe”, que será regido por este Regulamento, cujas disposições declaram os candidatos aceitar por ocasião da submissão de sua respectiva ficha de inscrição.
  
1. DO PROGRAMA
O Programa “Quem Sabe, Sabe” será um game diário, destinado aos participantes maiores de 18 (dezoito) anos de idade, com perguntas e respostas de múltiplas escolhas relacionadas a 09 (nove) temas (Ciências, Cinema e Tv, Cultura Pop, Esportes, Mundo, História, Literatura, Dicionário e Música). O game é baseado no conhecimento, estratégia e sorte dos competidores. 

Excepcionalmente, alguns programas em datas especiais a serem definidos pela Produção será permitida a participação de menores de 18 (dezoito) anos de idade, desde que devidamente representados e acompanhados por seus pais. Nestes casos excepcionais deverão ser observadas as seguintes condições por seus representantes em relação as gravações com os menores: a) apresentar certidão de matricula no colégio do menor; b) apresentar declaração de bom estado de saúde do menor; c) respeitar os horários escolares e não serem agendadas gravações à noite; d) o menor deverá sempre estar acompanhado de seu representante legal nas gravações do programa.

Tal Programa será gravado nos estúdios da emissora, com cenário de uma arena tecnológica, tendo 04 (quatro) participantes, representados por avatares de cores diferentes. Esses Participantes se enfrentarão em 03 (três) fases distintas durante 01 (uma) hora de programa, que será exibido e veiculado no período de segunda a sexta-feira.

O game é composto de 03 (três) fases. Durante a primeira e a segunda fase os participantes respondem sobre 09 (nove) temas (Ciências, Cinema e Tv, Cultura Pop, Esportes, Mundo, História, Literatura, Dicionário e Música). Dois deles passam para a terceira fase onde responderão sobre programas da TV CULTURA.

A primeira resposta do competidor é a que vale, não será aceita a troca da alternativa após sua escolha inicial.

As respostas certas valem pontos, sendo que, no programa, os pontos serão chamados de “EUREKAS”.

Ao final do jogo, o grande vencedor é aquele que acumulou a maior quantidade de “EUREKAS” (sistema de pontuação do jogo).
  
  
DAS REGRAS DO JOGO

PRIMEIRA FASE DO JOGO:
- Na primeira fase os participantes responderão perguntas de múltipla escolha, de forma alternada, sobre 5 (cinco) dos 9 (nove) temas do programa. A saber: Ciências, Cinema e Tv, Cultura Pop, Esportes, Mundo, História, Literatura, Dicionário e Música.

- Os temas para os participantes, nessa fase, serão sempre os mesmos garantindo assim igualdade de condições entre eles.

- A ordem dos participantes, nessa fase, será definida por sorteio, a exclusivo critério da Produção do Programa.

- A escolha dos temas e formulação das perguntas ficarão a cargo exclusivo da produção do programa.

- Cada pergunta tem 4 (quatro) alternativas e o participante terá no máximo 10 segundos para resposta de cada pergunta contados à partir da autorização dos apresentadores.

- A primeira resposta do competidor é a que vale, não aceitando troca da alternativa.

- As respostas certas valem 100 (cem) pontos, sendo que, no programa, os pontos serão chamados de “EUREKAS”. 

- No caso do participante errar a resposta ou de não responde-la dentro do tempo determinado, nessa fase o participante não perderá “EUREKAS”. Apenas deixará de ganhá-las.

- Ao final da primeira fase, os quatro participantes seguem para a segunda fase com o total de “EUREKAS” acumulados por cada um.


SEGUNDA FASE DO JOGO:
- Na segunda fase do programa os competidores participarão de rodadas de perguntas de múltipla escolha, no tabuleiro digital do “QUEM SABE, SABE!”.

- A ordem dos participantes é a mesma do início da primeira fase.

- A cada rodada os participantes acionarão uma roleta digital por meio de um tablet e o número sorteado pela roleta será o equivalente em casas percorridas por seus avatares no tabuleiro digital.

- O tabuleiro terá 24 (vinte e quatro) casas sendo 18 (dezoito) correspondentes às perguntas sobre os temas dos programas (Ciências, Cinema e Tv, Cultura Pop, Esportes, Mundo, História, Literatura, Dicionário e Música), e 6 (seis) “CASAS DE SORTE OU AZAR; 2 (duas) de “PERDE A VEZ”; 2 (duas) de “+20 EUREKAS” e 2 (duas) de “-20 EUREKAS”.

- Cada pergunta tem 4 (quatro) alternativas e o participante terá no máximo 10 (dez) segundos para resposta a partir da autorização dos apresentadores.

- As respostas certas valem 140 (cento e quarenta) pontos que, no programa, serão chamados de “EUREKAS”. 

- Em caso de erro ou de não responder dentro do tempo determinado, o participante perde 70 (setenta) “EUREKAS”, ou seja, metade dos pontos do valor da pergunta.

- Na última rodada de perguntas o participante poderá trocar a participação no tabuleiro digital pelo “ARRISCA TUDO”. Uma pergunta máster, formulada pela Produção, cujo acerto vale 500 (quinhentas) “EUREKAS” e o erro na resposta implica na perda total dos pontos acumulados pela participante.

- As perguntas serão sorteadas de forma aleatória, segundo os temas dispostos no tabuleiro digital.

- Na segunda fase do programa, os participantes contarão ainda com a ajuda da “Lojinha” do Quem Sabe, Sabe!, Uma espécie de loja virtual onde são oferecidos os seguintes produtos: “TROCAR A PERGUNTA”, “ELIMINAR 2 (DUAS) ALTERNATIVAS” e “JOGAR DE NOVO”.

- Cada produto na “Lojinha” custa 40 “EUREKAS” e o participante poderá ir às compras somente 2 (duas) vezes durante as rodadas do tabuleiro e nunca de forma acumulada, ou seja, não é permitido fazer 2 (duas) compras na mesma rodada.

- O “ARRISCA TUDO” não permite ajuda na resposta, portanto mesmo que o participante não tenha utilizado todas as suas compras na “Lojinha”, não poderá fazê-lo caso opte pela pergunta máster em lugar do tabuleiro na última rodada.

- Os dois participantes que acumularem mais “EUREKAS” ao fim da segunda fase do “QUEM SABE, SABE” seguem para disputa da terceira fase: o “DUELO DE TITÃS”.

- Se dois ou mais participantes terminarem em igualdade de pontos (eurekas)  ao final da segunda fase, os critérios de desempates obedecerão a seguinte ordem: (1) Quem menos usou a ajuda da loja virtual - (2) Quem mais pontuou na 1ª fase do game - (3) Quem não utilizou o Arrisca Tudo - (4) Se persistir o empate, os competidores jogam o dado virtual e quem tirar o número mais alto é o vencedor.


TERCEIRA FASE DO JOGO:
- Nesta fase os dois competidores com melhor pontuação responderão a rodadas de perguntadas baseadas em vídeos de programas da TV CULTURA.

- A escolha dos programas bem como das perguntas a serem respondidas pelos participantes ficará a cargo da Produção do programa. 

- O participante com maior pontuação na segunda fase do jogo será quem começara a responder na terceira fase.

- Cada pergunta tem 4 (quatro) alternativas e o participante terá no máximo 10 (dez) segundos para resposta de cada pergunta contados à partir da autorização dos apresentadores.

- Nesta fase, os participantes poderão contar com a ajuda de uma pessoa que cada participante indicou para participar do programa, por meio de “VÍDEO CONFERÊNCIA”.

- As respostas agora têm o seguinte valor:
  a) 200 (duzentas) “EUREKAS” na primeira rodada de perguntas.
  b) 400 (quatrocentas) “EUREKAS” na segunda rodada de perguntas.
  c) 700 (setecentas) “EUREKAS” na terceira rodada de perguntas.

- Em caso de o participante errar a resposta ou não responder dentro do tempo determinado, o participante perde metade dos pontos do valor da pergunta, ou seja:
  a) 100 (cem) “EUREKAS” na primeira rodada de perguntas.
  b) 200 (duzentas) “EUREKAS” na segunda rodada de perguntas.
  c) 350 (trezentas) “EUREKAS” na terceira rodada de perguntas.

- Vence o jogo aquele participante que, entre os dois finalistas, acumular o maior número de “EUREKAS” após as 3 (três) rodadas de perguntas.

- Em caso de empate entre os participantes, as regras para determinar o vencedor do programa obedecerão a seguinte ordem: (1) Quem menos usou produtos da “Lojinha do Quem Sabe, Sabe!” - (2) Quem mais pontuou na segunda fase do programa - (3) Quem mais pontuou na primeira fase do programa - (4) Quem não usou o “Arrisca Tudo” – (5) Quem não usou a “Vídeo Conferência” - (6) Se persistir o empate, os competidores jogam o dado virtual e quem tirar o número mais alto é o vencedor.


DEFINIÇÕES:
