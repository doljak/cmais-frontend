        <p>A interatividade é uma das principais características do QSS!. Na última fase da disputa, por exemplo, o participante pode contar com a ajuda de um amigo via internet. Ao mesmo tempo, enquanto o programa é exibido na TV, as pessoas também podem responder às mesmas perguntas pelo computador, em tempo real. Os interessados em participar do QSS! poderão se inscrever, em breve, na página do programa: <a href="http://cmais.com.br/qss" title="Quem Sabe, Sabe!">cmais.com.br/qss</a></p>
