    <h2>Uma grade para os novos tempos</h2>
    <div class="row-fluid">
      <p class="span6">Atenta às mudanças na forma de se transmitir e de se consumir mídia com os novos meios de tráfego de informações ou entretenimento, a TV Cultura apresenta sua grade de programação para 2013. Projetada para oferecer programas ao vivo, interativos e exclusivos, marcados antes de tudo pela qualidade, a TV Cultura compõe sua nova programação com atrações únicas, muitas das quais ao vivo e que permitem a participação interativa do público. Exemplo dessa nova tendênciano trabalho da emissora é a introdução da segunda tela, recurso que será inicialmente disponibilizado pelo Jornal da Cultura  e pelo novo game show Quem Sabe, Sabe! . O programa irá ao ar gravado, em suas primeiras edições, e depois ao vivo. O jogo depende do conhecimento da estratégia e da sorte de quatro competidores que se enfrentarão de segunda a sexta-feira numa disputa de perguntas e respostas.</p>
      <p class="span6">Além das atrações jornalísticas que já ocupam boa parte da grade, a emissora amplia seu espaço nessa área e em maio, inaugura o programa Poli , dedicado a entrevistas com personalidades. Será apresentado aos domingos à noite pela jornalista Maria Cristina Poli, recentemente eleita a melhor âncora da televisão brasileira. Outroprograma jornalístico é o Guia do Dia , que traz, ao vivo, notícias diversas, comentarios, orientações e agendas para informar o paulistano logo no início da manhã.Também é parte das novas atrações da emissora asérie Mad Men , a mais premiada dos últimos tempos na televisão americana, ganhadora de 15 Emmys e quatro Globos de Ouro. A sétima temporada da série DoctorWho , produzida pela BBC chega à tela da TV Cultura paraos adeptos da ficção científica e dos efeitos especiais.</p>
